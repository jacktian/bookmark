angular.module('bookmark',['ngResource','bookmark.controller','bookmark.filter','bookmark.service'])
.config(['$httpProvider','$resourceProvider',function($httpProvider,$resourceProvider) {
  // 添加一个默认的PUT请求到ngResource
  $resourceProvider.defaults.actions.update={
  	method:'PUT'
  };

  // Use x-www-form-urlencoded Content-Type
  // post和put的发送数据方式一样
  $httpProvider.defaults.headers.post['Content-Type'] = 'application/x-www-form-urlencoded;charset=utf-8';
  $httpProvider.defaults.headers.put['Content-Type'] = 'application/x-www-form-urlencoded;charset=utf-8';

  /**
   * The workhorse; converts an object to x-www-form-urlencoded serialization.
   * @param {Object} obj
   * @return {String}
   */ 
  var param = function(obj) {
    var query = '', name, value, fullSubName, subName, subValue, innerObj, i;
      
    for(name in obj) {
      value = obj[name];
        
      if(value instanceof Array) {
        for(i=0; i<value.length; ++i) {
          subValue = value[i];
          fullSubName = name + '[' + i + ']';
          innerObj = {};
          innerObj[fullSubName] = subValue;
          query += param(innerObj) + '&';
        }
      }
      else if(value instanceof Object) {
        for(subName in value) {
          subValue = value[subName];
          fullSubName = name + '[' + subName + ']';
          innerObj = {};
          innerObj[fullSubName] = subValue;
          query += param(innerObj) + '&';
        }
      }
      else if(value !== undefined && value !== null)
        query += encodeURIComponent(name) + '=' + encodeURIComponent(value) + '&';
    }
      
    return query.length ? query.substr(0, query.length - 1) : query;
  };

  // Override $http service's default transformRequest
  $httpProvider.defaults.transformRequest = [function(data,aa,bb,cc) {
  	console.log(param(data),aa,bb,cc);
    return angular.isObject(data) && String(data) !== '[object File]' ? param(data) : data;
  }];
}]);